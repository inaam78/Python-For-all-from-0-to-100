# For loop iterate over list
l=[22,43,545,3,2,5,55]
for i in l:
    print(i)
#for loop iterate over tuple
t=("inaam",1111, "d", 222,"f",22,"list",[1,2,4])
for i in t:
    print(i)
#for loop iterate over string
s="inaam"
for i in s:
    print(i)
#for loop iterate over dictionary
d={"inaam":1111, "d": 222,"f":22,"list":[1,2,4]}
for i in d:
    print(i) # it will print keys of dictionary